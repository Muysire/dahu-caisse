/* ---------- Identifiants ---------- */
function uid(){
  return "id-" + Math.random().toString(36).slice(2,10) + Date.now().toString(36).slice(-4);
}

/* ---------- Formatage ---------- */
const euro = n => new Intl.NumberFormat("fr-FR",{style:"currency",currency:"EUR"}).format(Number(n)||0);
const fdate = iso => new Intl.DateTimeFormat("fr-FR",{dateStyle:"short"}).format(new Date(iso));
const fdatetime = iso => new Intl.DateTimeFormat("fr-FR",{dateStyle:"short",timeStyle:"short"}).format(new Date(iso));

/* ---------- Sécurité ---------- */
const esc = s => String(s ?? "").replace(/[&<>"']/g, c => ({
  "&":"&amp;","<":"&lt;",">":"&gt;",'"':"&quot;","'":"&#39;"
}[c]));

/* ---------- Métier ---------- */
function stockStatus(stock,min){
  if(stock <= 0) return "out";
  if(stock <= min) return "low";
  return "ok";
}
function movementLabel(type){
  return {
    sale:"Vente", restock:"Réapprovisionnement", inventory:"Inventaire",
    correction:"Correction", initial:"Stock initial", import:"Import CSV"
  }[type] || type;
}

/* ---------- Visuel produit ----------
   Si la variante a une photo -> on l'affiche.
   Sinon -> emoji de la catégorie (bière, snack...).
   ------------------------------------ */
function productVisual(photoUrl, fallbackEmoji){
  if(photoUrl){
    return { cls:"photo", style:`background-image:url('${photoUrl}')`, content:"" };
  }
  return { cls:"emoji", style:"", content: fallbackEmoji || "✨" };
}

